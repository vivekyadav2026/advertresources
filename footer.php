    <script>
    class CyberBackground {
        constructor() {
            this.bgCanvas = document.getElementById('cyber-background-canvas');
            this.fgCanvas = document.getElementById('cyber-foreground-canvas');
            if (!this.bgCanvas || !this.fgCanvas) return;
            
            this.bgCtx = this.bgCanvas.getContext('2d');
            this.fgCtx = this.fgCanvas.getContext('2d');
            
            this.nodes = [];
            this.packets = [];
            this.waves = [];
            
            this.time = 0;
            this.scroll = {
                current: window.scrollY,
                target: window.scrollY,
                ease: 0.08
            };
            
            // Interactive Mouse Trail (Elastic Easing)
            this.mouse = { x: 0, y: 0, active: false };
            this.trail = [];
            this.maxTrailLength = 22;
            
            this.init();
            window.addEventListener('resize', () => this.resize());
            window.addEventListener('scroll', () => {
                this.scroll.target = window.scrollY;
            }, { passive: true });
            
            window.addEventListener('mousemove', (e) => {
                this.mouse.x = e.clientX;
                this.mouse.y = e.clientY;
                this.mouse.active = true;
                
                // Seed initial coordinates if empty to prevent jumping
                if (this.trail.length === 0) {
                    for (let i = 0; i < this.maxTrailLength; i++) {
                        this.trail.push({ x: this.mouse.x, y: this.mouse.y });
                    }
                }
            });
            
            window.addEventListener('mouseleave', () => {
                this.mouse.active = false;
            });
            
            this.animate();
        }
        
        init() {
            this.resize();
            
            // Initialize Nodes
            const nodeCount = Math.min(45, Math.floor((this.width * this.height) / 35000));
            for (let i = 0; i < nodeCount; i++) {
                this.nodes.push({
                    x: Math.random() * this.width,
                    y: Math.random() * this.height,
                    vx: (Math.random() - 0.5) * 0.3,
                    vy: (Math.random() - 0.5) * 0.3,
                    radius: Math.random() * 2 + 1.5,
                    pulse: Math.random() * Math.PI,
                    pulseSpeed: Math.random() * 0.03 + 0.01
                });
            }
            
            // Initialize active packets
            const packetCount = Math.min(12, Math.floor(nodeCount / 3));
            for (let i = 0; i < packetCount; i++) {
                const startNodeIndex = Math.floor(Math.random() * nodeCount);
                this.spawnPacket(startNodeIndex);
            }
            
            // Initialize Wave parameters (Vertical Waves)
            this.waves = [
                // Left side vertical waves
                {
                    amplitude: 20,
                    frequency: 0.006,
                    speed: 0.01,
                    color: 'rgba(60, 114, 252, 0.15)', // Blue
                    xPercent: 0.08,
                    scrollFactor: 0.2
                },
                {
                    amplitude: 30,
                    frequency: 0.004,
                    speed: -0.008,
                    color: 'rgba(224, 0, 155, 0.12)', // Purple
                    xPercent: 0.1,
                    scrollFactor: 0.3
                },
                // Right side vertical waves
                {
                    amplitude: 20,
                    frequency: 0.006,
                    speed: 0.012,
                    color: 'rgba(60, 114, 252, 0.15)', // Blue
                    xPercent: 0.9,
                    scrollFactor: 0.2
                },
                {
                    amplitude: 30,
                    frequency: 0.004,
                    speed: -0.009,
                    color: 'rgba(224, 0, 155, 0.12)', // Purple
                    xPercent: 0.92,
                    scrollFactor: 0.3
                }
            ];
        }
        
        spawnPacket(startIndex) {
            const startNode = this.nodes[startIndex];
            const neighbors = [];
            for (let j = 0; j < this.nodes.length; j++) {
                if (j === startIndex) continue;
                const dist = Math.hypot(startNode.x - this.nodes[j].x, startNode.y - this.nodes[j].y);
                if (dist < 180) {
                    neighbors.push(j);
                }
            }
            
            if (neighbors.length > 0) {
                const targetIndex = neighbors[Math.floor(Math.random() * neighbors.length)];
                this.packets.push({
                    from: startIndex,
                    to: targetIndex,
                    progress: Math.random(),
                    speed: Math.random() * 0.008 + 0.004
                });
            }
        }
        
        resize() {
            this.width = window.innerWidth;
            this.height = window.innerHeight;
            
            this.bgCanvas.width = this.width;
            this.bgCanvas.height = this.height;
            this.fgCanvas.width = this.width;
            this.fgCanvas.height = this.height;
        }
        
        animate() {
            this.scroll.current += (this.scroll.target - this.scroll.current) * this.scroll.ease;
            
            // Clear canvases
            this.bgCtx.clearRect(0, 0, this.width, this.height);
            this.fgCtx.clearRect(0, 0, this.width, this.height);
            
            this.time += 0.5;
            
            // Update Mouse Trail Elasticity
            if (this.mouse.active && this.trail.length > 0) {
                this.trail[this.trail.length - 1].x = this.mouse.x;
                this.trail[this.trail.length - 1].y = this.mouse.y;
            }
            
            // Elastic Glide trailing points
            for (let i = this.trail.length - 2; i >= 0; i--) {
                const p = this.trail[i];
                const next = this.trail[i + 1];
                p.x += (next.x - p.x) * 0.32;
                p.y += (next.y - p.y) * 0.32;
            }
            
            // Fade out trail when mouse is inactive
            if (!this.mouse.active && this.trail.length > 0) {
                this.trail.shift();
            }
            
            // Render Background Elements
            this.drawWaves(this.bgCtx);
            this.updateNodes(this.bgCtx);
            this.drawConnections(this.bgCtx);
            this.updatePackets(this.bgCtx);
            
            // Render Foreground Elements
            this.drawMouseTrail(this.fgCtx);
            
            requestAnimationFrame(() => this.animate());
        }
        
        drawWaves(ctx) {
            this.waves.forEach(wave => {
                ctx.beginPath();
                ctx.strokeStyle = wave.color;
                ctx.lineWidth = 1.5;
                ctx.shadowBlur = 8;
                ctx.shadowColor = wave.color;
                
                const baseX = this.width * wave.xPercent;
                const isMobile = this.width < 768;
                const amp = isMobile ? wave.amplitude * 0.3 : wave.amplitude;
                
                for (let y = 0; y <= this.height; y += 10) {
                    const angle = y * wave.frequency + (this.time * wave.speed) + (this.scroll.current * wave.scrollFactor * 0.005);
                    const x = baseX + Math.sin(angle) * amp;
                    if (y === 0) {
                        ctx.moveTo(x, y);
                    } else {
                        ctx.lineTo(x, y);
                    }
                }
                ctx.stroke();
            });
            ctx.shadowBlur = 0;
        }
        
        updateNodes(ctx) {
            this.nodes.forEach(node => {
                node.x += node.vx;
                node.y += node.vy;
                
                if (node.x < 0 || node.x > this.width) node.vx *= -1;
                if (node.y < 0 || node.y > this.height) node.vy *= -1;
                
                node.pulse += node.pulseSpeed;
                const opacity = 0.2 + Math.sin(node.pulse) * 0.12;
                
                ctx.beginPath();
                ctx.arc(node.x, node.y, node.radius, 0, Math.PI * 2);
                ctx.fillStyle = `rgba(96, 165, 250, ${opacity})`;
                ctx.fill();
            });
        }
        
        drawConnections(ctx) {
            ctx.lineWidth = 0.6;
            for (let i = 0; i < this.nodes.length; i++) {
                for (let j = i + 1; j < this.nodes.length; j++) {
                    const n1 = this.nodes[i];
                    const n2 = this.nodes[j];
                    const dist = Math.hypot(n1.x - n2.x, n1.y - n2.y);
                    
                    if (dist < 160) {
                        const opacity = (1 - dist / 160) * 0.05;
                        ctx.strokeStyle = `rgba(96, 165, 250, ${opacity})`;
                        ctx.beginPath();
                        ctx.moveTo(n1.x, n1.y);
                        ctx.lineTo(n2.x, n2.y);
                        ctx.stroke();
                    }
                }
            }
        }
        
        updatePackets(ctx) {
            for (let i = this.packets.length - 1; i >= 0; i--) {
                const packet = this.packets[i];
                const fromNode = this.nodes[packet.from];
                const toNode = this.nodes[packet.to];
                
                if (!fromNode || !toNode) {
                    this.packets.splice(i, 1);
                    continue;
                }
                
                packet.progress += packet.speed;
                
                if (packet.progress >= 1) {
                    packet.progress = 0;
                    packet.from = packet.to;
                    
                    const startNode = this.nodes[packet.from];
                    const neighbors = [];
                    for (let j = 0; j < this.nodes.length; j++) {
                        if (j === packet.from) continue;
                        const dist = Math.hypot(startNode.x - this.nodes[j].x, startNode.y - this.nodes[j].y);
                        if (dist < 160) {
                            neighbors.push(j);
                        }
                    }
                    
                    if (neighbors.length > 0) {
                        packet.to = neighbors[Math.floor(Math.random() * neighbors.length)];
                    } else {
                        packet.to = Math.floor(Math.random() * this.nodes.length);
                    }
                }
                
                const x = fromNode.x + (toNode.x - fromNode.x) * packet.progress;
                const y = fromNode.y + (toNode.y - fromNode.y) * packet.progress;
                
                ctx.beginPath();
                ctx.arc(x, y, 2, 0, Math.PI * 2);
                ctx.fillStyle = '#E0009B';
                ctx.shadowBlur = 6;
                ctx.shadowColor = '#E0009B';
                ctx.fill();
            }
            ctx.shadowBlur = 0;
        }
        
        drawMouseTrail(ctx) {
            if (this.trail.length < 1) return;
            
            ctx.shadowBlur = 10;
            
            // Draw trail of fading circles directly on the path (no snake movement)
            for (let i = 0; i < this.trail.length; i++) {
                const p = this.trail[i];
                
                // Calculate sizing and opacity factors
                const ratio = i / this.trail.length;
                const radius = ratio * 13.0 + 3.0; // Grows from 3.0px (tail) to 16.0px (head)
                const opacity = ratio * 0.7;
                
                ctx.beginPath();
                ctx.arc(p.x, p.y, radius, 0, Math.PI * 2);
                if (ratio > 0.5) {
                    ctx.strokeStyle = `rgba(224, 0, 155, ${opacity})`; // Pink
                    ctx.shadowColor = '#E0009B';
                } else {
                    ctx.strokeStyle = `rgba(60, 114, 252, ${opacity})`; // Blue
                    ctx.shadowColor = '#3C72FC';
                }
                ctx.lineWidth = 1.4;
                ctx.stroke();
            }
            
            // Draw double circles (concentric tracking rings) at the cursor position
            const head = this.trail[this.trail.length - 1];
            
            // 1. Inner Circle Ring
            ctx.shadowBlur = 8;
            ctx.shadowColor = '#E0009B';
            ctx.strokeStyle = 'rgba(224, 0, 155, 0.9)';
            ctx.lineWidth = 1.5;
            ctx.beginPath();
            ctx.arc(head.x, head.y, 7, 0, Math.PI * 2);
            ctx.stroke();
            
            // 2. Outer Circle Ring
            ctx.shadowColor = '#3C72FC';
            ctx.strokeStyle = 'rgba(60, 114, 252, 0.85)';
            ctx.lineWidth = 1.2;
            ctx.beginPath();
            ctx.arc(head.x, head.y, 18, 0, Math.PI * 2);
            ctx.stroke();
            
            ctx.shadowBlur = 0;
        }
    }

